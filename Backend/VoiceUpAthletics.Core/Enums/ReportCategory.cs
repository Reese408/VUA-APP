namespace VoiceUpAthletics.Core.Enums;

/// <summary>
/// Categories of issues that can be reported
/// </summary>
public enum ReportCategory
{
    /// <summary>
    /// Mental health concerns (depression, anxiety, burnout, etc.)
    /// </summary>
    MentalHealth,

    /// <summary>
    /// NCAA compliance violations
    /// </summary>
    ComplianceViolation,

    /// <summary>
    /// Harassment or bullying
    /// </summary>
    Harassment,

    /// <summary>
    /// Discrimination based on race, gender, religion, etc.
    /// </summary>
    Discrimination,

    /// <summary>
    /// Sexual misconduct or assault
    /// </summary>
    SexualMisconduct,

    /// <summary>
    /// Substance abuse concerns
    /// </summary>
    SubstanceAbuse,

    /// <summary>
    /// Academic misconduct or fraud
    /// </summary>
    AcademicMisconduct,

    /// <summary>
    /// Injury concerns or medical negligence
    /// </summary>
    InjuryConcerns,

    /// <summary>
    /// Coaching misconduct or abuse
    /// </summary>
    CoachingMisconduct,

    /// <summary>
    /// Other concerns not covered by specific categories
    /// </summary>
    Other
}
